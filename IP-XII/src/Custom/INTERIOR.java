        void enter(JLabel evt, boolean b) {
        String name = evt.getText();
        JLabel[][] label = new JLabel[][] {
            {L1, jLabel19, jLabel21, jLabel24, jLabel17, jLabel22, jLabel23},
            {jLabel25, jLabel26, jLabel27, jLabel29, jLabel30, jLabel31, jLabel32},
            {jLabel33, jLabel34, jLabel35, jLabel36, jLabel37, jLabel38, jLabel39},
        };
        try {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/car","root","sps");
            Statement stmt=con.createStatement();
            ResultSet rs = stmt.executeQuery("select count(*) from usercart where subpartsel = '" + name + "' and u_id = \"" + id + "\";");
            rs.first();
            if (rs.getInt("count(*)") != 0) {
                stmt.executeUpdate("delete from usercart where subpartsel ='" + name + "' and u_id = \"" + id + "\";");
            }
            else{
                rs = stmt.executeQuery("select * from interior where subpart ='" + name + "';");
                rs.first();
                String part = rs.getString("part"), subpart = rs.getString("subpart"), price = rs.getString("price");
                stmt.executeUpdate("insert into usercart values ('" + id + "', '" + part + "', '" + subpart + "', " + price + ");");
            }
        } catch (Exception e) {
        }
        int indexX, indexY;
        loop: for (indexX = 0; indexX < label.length; indexX ++) {
            for (indexY = 0; indexY < label[indexX].length; indexY ++) {
                if (label[indexX][indexY].equals(evt)) break loop;
            }
        }
        if (b) {
            for (JLabel x: label[indexX]) {
                if (x.getBackground().equals(Color.blue) && !x.equals(evt)) {
                    enter(x, false);
                    x.setBackground(Color.black);
                }
            }
        }
    }
