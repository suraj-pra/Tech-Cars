    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
         JLabel[][] label = new JLabel[][] {
          {L1, jLabel19, jLabel21, jLabel24, jLabel17, jLabel22, jLabel23},
            {jLabel25, jLabel26, jLabel27, jLabel29, jLabel30, jLabel31, jLabel32},
            {jLabel33, jLabel34, jLabel35, jLabel36, jLabel37, jLabel38, jLabel39},
        };
        try{
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/car","root","sps");
            Statement stmt=con.createStatement();
            String query="select * from interior;";
            ResultSet rs=stmt.executeQuery(query);
            rs.first();
            String init = rs.getString(2);
            rs.beforeFirst();
            int i = 0;
            while(rs.next()){
                if (!init.equals(rs.getString(2))) {
                    i = 0;
                    init = rs.getString(2);
                }
                switch (rs.getString(2)) {
                    case "CARGO":
                        label[0][i ++].setText(rs.getString(3));
                        break;
                    case "FLOOR_LINERS":
                        label[1][i ++].setText(rs.getString(3));
                        break;
                    case "STORAGE":
                        label[2][i ++].setText(rs.getString(3));
                        break;
                    
                }
            }
            rs.close();
            stmt.close();
            con.close();
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        for (JLabel[] x: label) {
            for (JLabel y: x) {
                y.setBackground(Color.black);
                if (y.getText().equals("")) {
                    y.setVisible(false);
                }
            }
        }
    }//GEN-LAST:event_formWindowOpened

