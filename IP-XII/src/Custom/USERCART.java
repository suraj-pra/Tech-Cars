    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        DefaultTableModel model2=(DefaultTableModel)T.getModel();
        try{
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/car","root","sps");
            Statement stmt=con.createStatement();
            String query="select * from USERCART where u_id = \"" + id + "\";";
            ResultSet rs=stmt.executeQuery(query);
            while(rs.next()){
                String PART=rs.getString("partsel");
                String SUBPART=rs.getString("subpartsel");
                int PRICE=rs.getInt("price"); 
                model2.addRow(new Object[] {PART,SUBPART,PRICE});
            }
            rs.close();
            stmt.close();
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_formWindowOpened

