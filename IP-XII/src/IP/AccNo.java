    private void payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payActionPerformed
        boolean cd = true;
        
        for (char c: acc.getText().toCharArray()){
            cd = cd && ("0123456789".indexOf(c) > -1);
        }
        if (cd) {
            if(acc.getText().length() == 12){
                FinalPay f = new FinalPay(this, null, n);
                f.setVisible(true);
                f.txt.setText("Online Payment:");
                f.name = name;
                f.cname = cname;
                f.prc.setText(prc.getText());
                f.fname = fname;
                f.id = id;
                f.ded = ded;
                f.paid = "Yes";
                this.setVisible(false);
            }
            else{
                Pane p = new Pane(this);
                p.txt.setText("I think acc no contains 12 numbers.");
                p.yes.setVisible(false);
                p.no.setVisible(false);
                p.setVisible(true);
            }
        }
        else {
            Pane p = new Pane(this);
            p.top.setText("Fill all details correctly.");
            p.txt.setText("Acc no must be numbers only.");
            p.yes.setVisible(false);
            p.no.setVisible(false);
            p.setVisible(true);
        }
    }//GEN-LAST:event_payActionPerformed

