    private void updlbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updlbMouseClicked
        try {
            String query = "Select pwd from user where u_id = \"" + id +"\";";
            java.sql.ResultSet rs = stmt.executeQuery(query);
            rs.first();
            String pwd = rs.getString("pwd");
            
            char gender = 0;
            if(M.isSelected())
                gender = 'M';
            if(F.isSelected())
                gender = 'F';
            if (new String(cpwd.getPassword()).equals(pwd)){
                if(!npwd.getPassword().equals("")){
                    String q = "Update user set fname = \"" + fname.getText() + "\", lname = \"" + lname.getText() + "\", gender = \"" + gender + "\", DOB = \"" + year.getText() + "/" + month.getText() + "/" + date.getText() + "\", pwd = \"" + new String(npwd.getPassword()) + "\" where u_id = \"" + id +"\";";
                    stmt.executeUpdate(q);
                    uacc.ids = id;
                    uacc.setVisible(true);
                    this.setVisible(false);

                    Pane p = new Pane(this);
                    p.txt.setText("Your details are successfully updated.");
                    p.yes.setVisible(false);
                    p.no.setVisible(false);
                    p.setVisible(true);
                }
                else{
                    Pane p = new Pane(this);
                    p.txt.setText("Fill your new password first.");
                    p.yes.setVisible(false);
                    p.no.setVisible(false);
                    p.setVisible(true);
                }
            }
            else{
                Pane p = new Pane(this);
                p.txt.setText("You didn't entered the correct password.");
                p.yes.setVisible(false);
                p.no.setVisible(false);
                p.setVisible(true);
            }
        }
        catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updlbMouseClicked

