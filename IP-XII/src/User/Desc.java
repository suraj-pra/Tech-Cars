    private void fnameMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fnameMouseClicked
        if(!id.getText().equals("")){
            try {
                UserAccount uacc = new UserAccount(id.getText(), 3, null, null, this);

                uacc.stmt = stmt;
                uacc.setVisible(true);
                this.setVisible(false);
            }
            catch (Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, e);
            }
        }
        else {
            Both.Login lg = new Both.Login(3, null, null, this);
            lg.setVisible(true);
            lg.custadm.setVisible(false);
            lg.idlb.setText("UserID:");
            lg.code = "User";
            lg.ch = 'u';
            this.setVisible(false);
        }
    }//GEN-LAST:event_fnameMouseClicked

    private void backMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_backMouseClicked
        if(m != null){
            m.fname.setText(fname.getText());
            m.id.setText(id.getText());
            m.setVisible(true);
        }
        else{
            UserHome u = new UserHome();
            u.stmt = stmt;
            u.fname.setText(fname.getText());
            u.id.setText(id.getText());
            u.setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_backMouseClicked

