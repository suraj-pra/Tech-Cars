    private void paidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_paidActionPerformed
        try{
            if(!bno.getText().equals("")){
                if(tb.getValueAt(tb.getSelectedRow(), 8).equals("No")){
                    stmt.executeUpdate("update history set paid = \"Yes\" where b_no = \"" + bno.getText() + "\";");
                    tb.setValueAt("Yes", tb.getSelectedRow(), 8);
                }
                else{
                    Pane p = new Pane(this);
                    p.txt.setText("It is already paid.");
                    p.yes.setVisible(false);
                    p.no.setVisible(false);
                    p.setVisible(true);
                }
            }
            else{
                Pane p = new Pane(this);
                p.txt.setText("Please select something.");
                p.yes.setVisible(false);
                p.no.setVisible(false);
                p.setVisible(true);
            }
        }
        catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_paidActionPerformed

