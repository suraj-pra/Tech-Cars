    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            DefaultTableModel t = (DefaultTableModel)tb.getModel();
            
            ResultSet rs = stmt.executeQuery("select * from history;");
            while(rs.next()){
                t.addRow(new Object[]{rs.getInt("b_no"), rs.getString("u_id"), rs.getString("u_name"), rs.getString("time"), rs.getString("car"), rs.getString("cname"), rs.getInt("prc"), rs.getInt("rating"), rs.getString("paid")});
            }
            rs.close();
        }
        catch(Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formWindowOpened

