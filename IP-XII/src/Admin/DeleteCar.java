    public void dlt(){
        try {
            String query = "Delete from " + brand + " where submodel = \"" + sub +"\";";
            stmt.executeUpdate(query);
            
            new java.io.File(opimg).delete();
            new java.io.File(optxt).delete();

            c.dispose();
            
            AdminHome ad = new AdminHome();
            ad.setVisible(true);
            ad.stmt = stmt;
            this.dispose();
        }
        catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, e);
        }
    }
    
