    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        if(evt.getKeyCode() == 9){
            if(goback.getBorder() == null){
                goback.setBorder(new javax.swing.border.LineBorder(java.awt.Color.black, 2));
                delete.setBorder(null);
            }
            else if(delete.getBorder() == null){
                delete.setBorder(new javax.swing.border.LineBorder(java.awt.Color.black, 2));
                goback.setBorder(null);
            }
        }
        if(evt.getKeyCode() == 32){
            if(goback.getBorder() != null){
                c.setVisible(true);
                c.setEnabled(true);
                this.dispose();
            }
            else if(delete.getBorder() != null)
                dlt();
        }
    }//GEN-LAST:event_formKeyPressed

